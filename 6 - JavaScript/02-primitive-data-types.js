// Primitive Data Types

// Camel case naming convention

let firstName = "John";
console.log("Firstname:", firstName);
// Type of operator
console.log("Data Type:", typeof firstName);


// 2. Number: numerical data. (positive, negative, decimal)
let age = 30;
console.log("Age:", age);
console.log("Data Type:", typeof age);

// Boolean: either true or false
let isStudent = true;
console.log("Is Student", isStudent);
console.log("Data Type:", typeof isStudent);

// 4. Null: intentional absence of value
let car = null;
console.log("Car:", car);
console.log("Data Type:", typeof car);
car = "Toyota Supra";
console.log("Car:", car);
console.log("Data Type:", typeof car);

// 5. Undefined: unintentional absence of value
let city;
console.log("City:", city);
console.log("Data Type:", typeof city)



